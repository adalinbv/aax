#if HAVE_CONFIG_H
#include "config.h"
#endif

#include "arch_simd.h"

#ifdef __SSE3__

void
_batch_mul_value_sse3(void* data, unsigned bps, unsigned int num, float f)
{
   unsigned int i = num;

   if (num)
   {
      switch (bps)
      {
      case 1:
      {
         int8_t* d = (int8_t*)data;
         do {
            *d++ *= f;
         }
         while (--i);
         break;
      }
      case 2:
      {
         int16_t* d = (int16_t*)data;
         do {
            *d++ *= f;
         }
         while (--i);
         break;
         }
      case 4:
      {
         int32_t* d = (int32_t*)data;
         do {
            *d++ *= f;
         }
         while (--i);
         break;
      }
      default:
         break;
      }
   }
}

#endif /* SSE3 */

